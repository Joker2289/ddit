package e_oop;

public class Calculator {
	
	
	
	//더하기 
	double add(double a, double b){
		return a+b;
	}
	//빼기
	double sub(double a, double b){
		return a-b;
	}
	//곱하기
	double multiply(double a, double b){
		return a*b;
	}
	//나누기
	double divide(double a, double b){
		return a/b;
	}
	//나머지
	double remainder(double a, double b){
		return a%b;
	}
	

	
	
	
		
	
	
}
